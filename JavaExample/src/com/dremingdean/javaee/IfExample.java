package com.dremingdean.javaee;

public class IfExample {

		public void ifStatement(int score){
		if(score>90){
			System.out.println("A 입니다.");
		}else if(score>80){
			System.out.println("B 입니다.");
		}else if(score>70){
		System.out.println("C 입니다.");
		}else{
			System.out.println("F 입니다.");
		}
	}
		
}

